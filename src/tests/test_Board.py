import unittest
from classes.board import Board

class TestBoard(unittest.TestCase):
    
    def test_dump(self):
        game_board = Board()
        self.assertEqual(game_board.board, [[0] * 20 for _ in range(15)])
        game_board.dump()
        self.assertNotEqual(game_board.board, [[0] * 20 for _ in range(15)])
    
    def test_isBlockValid(self):
        game_board = Board()
        self.assertEqual(game_board.isBlockValid(20, 20), False)
        x = game_board.cur_block.x
        game_board.dump()
        self.assertEqual(game_board.isBlockValid(x, 0), False)
    
